package com.system.entity;

import java.math.BigDecimal;

public class Hotel {
	private BigDecimal H_ID;
	private String H_NAME;
	private String H_LOC;
	private String H_PHONE;
	private String H_INFO;
	private String H_IMG;
	public BigDecimal getH_ID() {
		return H_ID;
	}
	public void setH_ID(BigDecimal h_ID) {
		H_ID = h_ID;
	}
	public String getH_NAME() {
		return H_NAME;
	}
	public void setH_NAME(String h_NAME) {
		H_NAME = h_NAME;
	}
	public String getH_LOC() {
		return H_LOC;
	}
	public void setH_LOC(String h_LOC) {
		H_LOC = h_LOC;
	}
	public String getH_PHONE() {
		return H_PHONE;
	}
	public void setH_PHONE(String h_PHONE) {
		H_PHONE = h_PHONE;
	}
	public String getH_INFO() {
		return H_INFO;
	}
	public void setH_INFO(String h_INFO) {
		H_INFO = h_INFO;
	}
	public String getH_IMG() {
		return H_IMG;
	}
	public void setH_IMG(String h_img) {
		H_IMG = h_img;
	}
	public Hotel(BigDecimal h_ID, String h_NAME, String h_LOC, String h_PHONE,
			String h_INFO, String h_img) {
		super();
		H_ID = h_ID;
		H_NAME = h_NAME;
		H_LOC = h_LOC;
		H_PHONE = h_PHONE;
		H_INFO = h_INFO;
		H_IMG = h_img;
	}
	public Hotel() {
		super();
		// TODO Auto-generated constructor stub
	}
	@Override
	public String toString() {
		return "Hotel [H_ID=" + H_ID + ", H_NAME=" + H_NAME + ", H_LOC="
				+ H_LOC + ", H_PHONE=" + H_PHONE + ", H_INFO=" + H_INFO
				+ ", H_img=" + H_IMG + "]";
	}

	
}
