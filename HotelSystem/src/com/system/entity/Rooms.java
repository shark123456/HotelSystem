package com.system.entity;

import java.math.BigDecimal;

public class Rooms {
	private BigDecimal RO_ID;
	private String RO_NO;
	private String RO_CLASS;
	private String RO_EMPRY;
	private String RO_IMG;
	private BigDecimal RO_PRICE;
	
	
	public BigDecimal getRO_ID() {
		return RO_ID;
	}
	public void setRO_ID(BigDecimal rO_ID) {
		RO_ID = rO_ID;
	}
	public String getRO_NO() {
		return RO_NO;
	}
	public void setRO_NO(String rO_NO) {
		RO_NO = rO_NO;
	}
	public String getRO_CLASS() {
		return RO_CLASS;
	}
	public void setRO_CLASS(String rO_CLASS) {
		RO_CLASS = rO_CLASS;
	}
	public String getRO_EMPRY() {
		return RO_EMPRY;
	}
	public void setRO_EMPRY(String rO_EMPRY) {
		RO_EMPRY = rO_EMPRY;
	}
	public String getRO_IMG() {
		return RO_IMG;
	}
	public void setRO_IMG(String rO_IMG) {
		RO_IMG = rO_IMG;
	}
	public BigDecimal getRO_PRICE() {
		return RO_PRICE;
	}
	public void setRO_PRICE(BigDecimal rO_PRICE) {
		RO_PRICE = rO_PRICE;
	}
	public Rooms(BigDecimal rO_ID, String rO_NO, String rO_CLASS,
			String rO_EMPRY, String rO_IMG, BigDecimal rO_PRICE) {
		super();
		RO_ID = rO_ID;
		RO_NO = rO_NO;
		RO_CLASS = rO_CLASS;
		RO_EMPRY = rO_EMPRY;
		RO_IMG = rO_IMG;
		RO_PRICE = rO_PRICE;
	}
	public Rooms() {
		super();
		// TODO Auto-generated constructor stub
	}
	@Override
	public String toString() {
		return "Rooms [RO_ID=" + RO_ID + ", RO_NO=" + RO_NO + ", RO_CLASS="
				+ RO_CLASS + ", RO_EMPRY=" + RO_EMPRY + ", RO_IMG=" + RO_IMG
				+ ", RO_PRICE=" + RO_PRICE + "]";
	}
	
	
	

}
